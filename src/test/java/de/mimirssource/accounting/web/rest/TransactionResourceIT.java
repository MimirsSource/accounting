package de.mimirssource.accounting.web.rest;

import de.mimirssource.accounting.AccountingApp;
import de.mimirssource.accounting.domain.Transaction;
import de.mimirssource.accounting.repository.TransactionRepository;
import de.mimirssource.accounting.service.TransactionService;
import de.mimirssource.accounting.web.rest.errors.ExceptionTranslator;

import org.junit.jupiter.api.BeforeEach;
import org.junit.jupiter.api.Test;
import org.mockito.MockitoAnnotations;
import org.springframework.beans.factory.annotation.Autowired;
import org.springframework.boot.test.context.SpringBootTest;
import org.springframework.data.web.PageableHandlerMethodArgumentResolver;
import org.springframework.http.MediaType;
import org.springframework.http.converter.json.MappingJackson2HttpMessageConverter;
